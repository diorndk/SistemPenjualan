    private void btnSupplierBaruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupplierBaruActionPerformed
        String txtBtnSup = btnSupplierBaru.getText();
        String newKode = "";
        if(txtBtnSup.equals("Baru")){
            flagUpdate = false;
            btnSupplierBaru.setText("Cancel");
            String lastKode = dao.getLastKodeSupplier();
            if(!lastKode.equals("")){
                String lastSeq = lastKode.substring(2);
                String newSeqKode = ""+(Integer.parseInt(lastSeq)+1);
                String nol = "";
                if(newSeqKode.length() == 1){
                    nol = "00";
                } else if(newSeqKode.length() == 2){
                    nol = "0";
                } else if(newSeqKode.length() == 3){
                    nol = "";
                }

                newKode = "SU"+nol+newSeqKode;
            }else{
                newKode = "SU001";
            }
            
            txtKodeSupplier.setText(newKode);
            txtNamaSupplier.setText("");
            txtAlamat.setText("");
            txtKota.setText("");
            txtNoTlp.setText("");
            btnHapusSupplier.setEnabled(false);
        }else{
            flagUpdate = true;
            btnSupplierBaru.setText("Baru");
            txtKodeSupplier.setText("");
            btnHapusSupplier.setEnabled(true);
        }
    }//GEN-LAST:event_btnSupplierBaruActionPerformed

