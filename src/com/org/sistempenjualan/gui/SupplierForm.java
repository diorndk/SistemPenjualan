    private void btnSimpanSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanSupplierActionPerformed
        boolean result = false;
        int confirm = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menyimpan data?","Simpan Data",JOptionPane.YES_NO_OPTION);
        try{
            if(confirm==0){
                if(!flagUpdate){
                    entity.setKodeSupplier(txtKodeSupplier.getText());
                    entity.setNamaSupplier(txtNamaSupplier.getText());
                    entity.setAlamatSupplier(txtAlamat.getText());
                    entity.setKota(txtKota.getText());
                    entity.setTlpSupplier(txtNoTlp.getText());
                    entity.setNikSession(nikSession);
                    result = dao.addSupplier(entity);
                    if(result){
                        JOptionPane.showMessageDialog(rootPane,"Data Berhasil disimpan!");
                        clearForm();
                    }else{
                        JOptionPane.showMessageDialog(rootPane,"Error Simpan Supplier");
                    }
                }else{
                    entity.setKodeSupplier(txtKodeSupplier.getText());
                    entity.setNamaSupplier(txtNamaSupplier.getText());
                    entity.setAlamatSupplier(txtAlamat.getText());
                    entity.setKota(txtKota.getText());
                    entity.setTlpSupplier(txtNoTlp.getText());
                    entity.setNikSession(nikSession);
                    result = dao.editSupplier(entity);
                    if(result){
                        JOptionPane.showMessageDialog(rootPane,"Data Berhasil diupdate!");
                        clearForm();
                    }else{
                        JOptionPane.showMessageDialog(rootPane,"Error Update Supplier");
                    }
                }
            }
        }catch(Exception e){
            try {
                throw new Exception(e);
            } catch (Exception ex) {
                Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSimpanSupplierActionPerformed

